    public function filtered(Request $request)
    {

        $search = '%' . $request->input('description') . '%';
        $logs = Log::where('description', 'like', $search)->orderBy('date', 'desc')->get();

        return view('admin.log.filtered', [
            'logs' => $logs,
        ]);
    }

